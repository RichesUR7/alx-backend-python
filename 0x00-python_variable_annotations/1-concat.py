#!/usr/bin/env python3
"""
Module contains a function that concatenates twi strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Takes two strings and return their concatenated string.

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: Concatenated string.
    """
    return str1 + str2
